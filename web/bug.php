<br>
<br>
<h3>Quali sono i nostri obiettivi.</h3>
Favorire il formarsi di gruppi locali di persone - i B.U.G. per l'appunto - che si trovino dal vivo, e non solo on-line, a giocare insieme per qualche ora.<br>
